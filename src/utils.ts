export function monthNameToNumber(monthName: string) {
  const monthNames = [
    "january",
    "february",
    "march",
    "april",
    "may",
    "june",
    "july",
    "august",
    "september",
    "october",
    "november",
    "december",
  ];

  return monthNames.indexOf(monthName.toLowerCase()) + 1;
}
